import numpy as np
import matplotlib.pyplot as plt

# Input data (OR gate)
x1 = np.array([0, 0, 1, 1])
x2 = np.array([0, 1, 0, 1])
y  = np.array([0, 1, 1, 1])

# Step function
def step_function(z):
    return 1 if z >= 0 else 0

# Initialize weights and bias
w1 = 0.0
w2 = 0.0
b  = 0.0
lr = 0.1
epochs = 10

# Training
for epoch in range(epochs):
    for i in range(len(x1)):
        z = w1 * x1[i] + w2 * x2[i] + b
        y_pred = step_function(z)
        err = y[i] - y_pred

        w1 = w1 + lr * err * x1[i]
        w2 = w2 + lr * err * x2[i]
        b  = b  + lr * err

print("Final weights and bias:")
print("w1 =", w1, "w2 =", w2, "b =", b)

# ---- Plot graph ----
plt.figure()

# Plot points
plt.scatter(x1, x2)
for i in range(len(x1)):
    plt.text(x1[i] + 0.02, x2[i] + 0.02, str(y[i]))

# Decision boundary: w1*x1 + w2*x2 + b = 0
x_vals = np.array([-0.2, 1.2])
y_vals = (-w1 * x_vals - b) / w2

plt.plot(x_vals, y_vals)
plt.xlabel("x1")
plt.ylabel("x2")
plt.title("Perceptron Decision Boundary (OR Gate)")
plt.show()

