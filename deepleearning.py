import numpy as np

#training data
x=np.array([1,2,3,4]) #stuudy hrs
y=np.array([0,0,1,1]) #pass or fail
x

y

w=0;b=0;lr=1;epochs=5

"""# define activation step **function**"""

def step_function(z) :
  return 1 if z>=0 else 0

#traiing loop
w=0;b=0;lr=1;epochs=5
print("initial weight",w)
print("inital bias",b)
print("learning rate",lr)

for epoch in range(epochs):
  print(f"\n epoch {epoch+1}")
  for i in range(len(x)):
    #1.compute weighted sum
    z=w*x[i]+b
    #2.apply activation function
    y_pred=step_function(z)
    #3.compute error
    err=y[i]-y_pred
    #4.update weigth
    w=w+lr*err*x[i]
    #5.update bias
    b=b+lr*err
    print(f"input {x[i]} ,pred_val {y_pred},actual val :{y[i]},w:{w},b:{b}")

#testing the model
test_val=3.1
z=w*test_val+b
prediction=step_function(z)
print(f"for std hrs {test_val},prediction is {prediction}")

